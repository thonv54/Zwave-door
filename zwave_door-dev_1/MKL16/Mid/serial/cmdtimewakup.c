/**
 * @func   HandleCmdTimeWakup 
 * @brief  Setup time interval wakeup
 * @param  None
 * @retval None
 */
void
HandleCmdTimeWakup(
    BYTE option,
    CMD_BUFFER_P pCmd,
    BYTE length
) {
    switch(pCmd->scommon.cmdtype) {
    case CMD_TYPE_SET:
    {
        WORD time;
        time = (pCmd->stimeconfig.timeH << 8) + pCmd->stimeconfig.timeL;
        SetConfigParam(PARAM_TIME_WAKEUP, &time);
    }   break;
    case CMD_TYPE_GET:
    {
        
    }   break;

    default:
        break;
    }
}

